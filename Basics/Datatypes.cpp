#include<iostream>
using namespace std;
int main()
{
	//Integer DataType
	int a=5;
	cout<<"Number: "<<a<<endl;
	cout<<"Size of Integer: "<<sizeof(a)<<" bytes"<<endl;
	//Character DataType
	char b='*';
	cout<<"Number: "<<b<<endl;
	cout<<"Size of Character: "<<sizeof(b)<<" bytes"<<endl;
	//Boolean DataType
	bool c=true;
	cout<<"Number: "<<c<<endl;
	cout<<"Size of Boolean: "<<sizeof(c)<<" bytes"<<endl;
	//Floating DataType
	float d=5.657;
	cout<<"Number: "<<d<<endl;
	cout<<"Size of Float: "<<sizeof(d)<<" bytes"<<endl;
	//Double Floating DataType
	double e=3.95678939754;
	cout<<"Number: "<<e<<endl;
	cout<<"Size of Double: "<<sizeof(e)<<" bytes"<<endl;
	return 0;
}
